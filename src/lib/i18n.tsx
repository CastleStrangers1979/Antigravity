interface LanguageContextType {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string) => string;
  isRTL: boolean;
  formatDate: (date: Date | string | number, options?: Intl.DateTimeFormatOptions) => string;
  formatNumber: (num: number, options?: Intl.NumberFormatOptions) => string;
  formatCurrency: (num: number, currency?: string) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>('ar');

  useEffect(() => {
    const savedLang = localStorage.getItem('language') as Language;
    if (savedLang && ['ar', 'en', 'nl', 'ku', 'tr'].includes(savedLang)) {
      setLanguageState(savedLang);
    }
  }, []);

  const setLanguage = (lang: Language) => {
    setLanguageState(lang);
    localStorage.setItem('language', lang);
  };

  const t = (key: string): string => {
    const translation = translations[key];
    if (!translation) return key;
    return translation[language] || translation.en || key;
  };

  const isRTL = language === 'ar' || language === 'ku';

  const getLocale = () => {
    switch (language) {
      case 'ar': return 'ar-SA';
      case 'nl': return 'nl-NL';
      case 'ku': return 'ku-TR';
      case 'tr': return 'tr-TR';
      default: return 'en-US';
    }
  };

  const formatDate = (date: Date | string | number, options?: Intl.DateTimeFormatOptions): string => {
    try {
      const d = new Date(date);
      if (isNaN(d.getTime())) return String(date);
      return new Intl.DateTimeFormat(getLocale(), options || {
        year: 'numeric', month: 'long', day: 'numeric'
      }).format(d);
    } catch (e) {
      return String(date);
    }
  };

  const formatNumber = (num: number, options?: Intl.NumberFormatOptions): string => {
    try {
      return new Intl.NumberFormat(getLocale(), options).format(num);
    } catch (e) {
      return String(num);
    }
  };

  const formatCurrency = (num: number, currency: string = 'EUR'): string => {
    try {
      return new Intl.NumberFormat(getLocale(), {
        style: 'currency',
        currency: currency
      }).format(num);
    } catch (e) {
      return `${num} ${currency}`;
    }
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, isRTL, formatDate, formatNumber, formatCurrency }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}

export type { Language };
